/**
 * _strlen - returns the length of a string.
 * @str: string pointer
 * return: 1
*/
int _strlen(char *str)
{
	int i;

	for (i = 0; str[i] != 0; i++)
	return (i);
}
/**
 * _strlenc - returns the length of a constant char str
 * @str: string pointer
 * return: 1
*/
int _strlenc(const char *str)
{
	int i;

	for (i = 0; str[i] != 0; i++)
	return (i);
}
